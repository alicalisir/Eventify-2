"""
Evaluate Persona Model on Held-out Synthetic Test Set
=====================================================

Loads the trained CatBoost artifact, runs it on the unseen test population
(generated by generate_test_data.py), and emits:

  outputs/reports/evaluation_report.md   — human-readable summary
  outputs/reports/per_class_metrics.csv  — precision/recall/F1 per persona
  outputs/reports/confusion_matrix.png   — heatmap
  outputs/reports/feature_importance.png — CatBoost gain importance
  outputs/reports/shap_summary.png       — SHAP beeswarm (top 25 features)

This script is *evaluation-only* — it never touches training data.
"""

from __future__ import annotations

import json
import logging
import time
from pathlib import Path

import numpy as np
import pandas as pd
import matplotlib
matplotlib.use("Agg")           # headless
import matplotlib.pyplot as plt
from catboost import CatBoostClassifier
from sklearn.metrics import (
    accuracy_score, f1_score, classification_report,
    confusion_matrix, top_k_accuracy_score,
)

import pipeline_config as cfg
import feature_engineering as fe

logging.basicConfig(
    level=logging.INFO,
    format="%(asctime)s | %(levelname)s | %(name)s | %(message)s",
)
log = logging.getLogger("eval")


# ============================================================================
# load
# ============================================================================

def load_model_and_metadata():
    if not cfg.MODEL_FILE.exists():
        raise FileNotFoundError(
            f"No trained model at {cfg.MODEL_FILE}. "
            f"Run train_catboost_persona_model.py first."
        )
    model = CatBoostClassifier()
    model.load_model(str(cfg.MODEL_FILE))
    classes = json.loads(cfg.LABEL_ENCODER_FILE.read_text())["classes"]
    feature_set = json.loads(cfg.LABEL_ENCODER_FILE.read_text()).get("feature_set", "telemetry")
    feature_cols = json.loads(cfg.FEATURE_LIST_FILE.read_text())
    return model, classes, feature_cols, feature_set


def load_test_features(feature_set: str) -> pd.DataFrame:
    log.info(f"reading raw test data from {cfg.TEST_RAW_DIR}")
    include_eps = feature_set == "with_episodes"
    feats = fe.features_from_directory(cfg.TEST_RAW_DIR, include_episode_features=include_eps)
    feats = fe.attach_persona_labels(feats, cfg.TEST_RAW_DIR / "users.csv")
    feats = feats.dropna(subset=["persona"])
    feats.to_csv(cfg.TEST_FEATURES_CSV, index=False)
    log.info(f"  test feature matrix: {feats.shape}")
    return feats


# ============================================================================
# metrics
# ============================================================================

def compute_metrics(model, df_test, classes, feature_cols):
    class_to_idx = {c: i for i, c in enumerate(classes)}
    y_true = df_test["persona"].map(class_to_idx).to_numpy()
    X = df_test[feature_cols].to_numpy(dtype=float)

    y_pred  = model.predict(X).flatten().astype(int)
    proba   = model.predict_proba(X)

    acc       = accuracy_score(y_true, y_pred)
    macro_f1  = f1_score(y_true, y_pred, average="macro")
    micro_f1  = f1_score(y_true, y_pred, average="micro")
    weighted_f1 = f1_score(y_true, y_pred, average="weighted")

    # Top-2 accuracy (only meaningful when more than 2 classes)
    top2 = top_k_accuracy_score(y_true, proba, k=2,
                                labels=list(range(len(classes))))

    log.info(f"TEST accuracy   = {acc:.4f}")
    log.info(f"TEST macro-F1   = {macro_f1:.4f}")
    log.info(f"TEST micro-F1   = {micro_f1:.4f}")
    log.info(f"TEST weighted-F1 = {weighted_f1:.4f}")
    log.info(f"TEST top-2 acc  = {top2:.4f}")

    rep = classification_report(
        y_true, y_pred, target_names=classes, digits=3,
        zero_division=0, output_dict=True,
    )

    per_class = pd.DataFrame({
        cl: {"precision": rep[cl]["precision"],
             "recall":    rep[cl]["recall"],
             "f1-score":  rep[cl]["f1-score"],
             "support":   rep[cl]["support"]}
        for cl in classes
    }).T
    per_class.to_csv(cfg.PER_CLASS_CSV)
    log.info(f"  saved per-class metrics -> {cfg.PER_CLASS_CSV}")

    cm = confusion_matrix(y_true, y_pred, labels=list(range(len(classes))))
    return {
        "accuracy": acc, "macro_f1": macro_f1, "micro_f1": micro_f1,
        "weighted_f1": weighted_f1, "top2_accuracy": top2,
    }, per_class, cm, y_true, y_pred, proba


def plot_confusion(cm, classes, out_path):
    fig, ax = plt.subplots(figsize=(10, 8))
    cm_norm = cm / np.maximum(1, cm.sum(axis=1, keepdims=True))
    im = ax.imshow(cm_norm, cmap="Blues", vmin=0, vmax=1)
    ax.set_xticks(range(len(classes))); ax.set_xticklabels(classes, rotation=45, ha="right")
    ax.set_yticks(range(len(classes))); ax.set_yticklabels(classes)
    ax.set_xlabel("Predicted"); ax.set_ylabel("True")
    ax.set_title("Confusion Matrix (row-normalized)")
    for i in range(len(classes)):
        for j in range(len(classes)):
            ax.text(j, i, f"{cm_norm[i,j]:.2f}",
                    ha="center", va="center",
                    color="white" if cm_norm[i,j] > 0.5 else "black",
                    fontsize=8)
    plt.colorbar(im, ax=ax, fraction=0.046, pad=0.04)
    plt.tight_layout()
    plt.savefig(out_path, dpi=120)
    plt.close()
    log.info(f"  saved confusion matrix -> {out_path}")


