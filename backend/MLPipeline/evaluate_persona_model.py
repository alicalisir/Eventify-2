# interpretability
# ============================================================================

def plot_feature_importance(model, feature_cols, out_path, top_n=25):
    imp = model.get_feature_importance(type="FeatureImportance")
    df = (pd.DataFrame({"feature": feature_cols, "importance": imp})
            .sort_values("importance", ascending=True)
            .tail(top_n))
    fig, ax = plt.subplots(figsize=(8, 0.3 * len(df) + 1))
    ax.barh(df["feature"], df["importance"], color="#3a86ff")
    ax.set_xlabel("Importance (gain)")
    ax.set_title(f"CatBoost Feature Importance — top {top_n}")
    plt.tight_layout()
    plt.savefig(out_path, dpi=120)
    plt.close()
    log.info(f"  saved feature importance -> {out_path}")
    return df.iloc[::-1].reset_index(drop=True)


def shap_summary(model, df_test, feature_cols, classes, out_path, max_users=300):
    """SHAP beeswarm of top features for the most populous predicted class."""
    try:
        import shap
    except ImportError:
        log.warning("shap not installed; skipping SHAP summary")
        return None

    sample = df_test.sample(n=min(max_users, len(df_test)),
                            random_state=cfg.RANDOM_SEED_TEST)
    X = sample[feature_cols].to_numpy(dtype=float)

    # CatBoost SHAP via TreeExplainer
    log.info("  computing SHAP values ...")
    t0 = time.time()
    explainer = shap.TreeExplainer(model)
    shap_values = explainer.shap_values(X)   # shape: (samples, features, classes)
    log.info(f"    shap done in {time.time()-t0:.1f}s, shape={np.asarray(shap_values).shape}")

    # average absolute SHAP across classes -> per-feature importance
    sv = np.asarray(shap_values)
    if sv.ndim == 3:
        # multiclass
        mean_abs = np.mean(np.abs(sv), axis=(0, 2))
    else:
        mean_abs = np.mean(np.abs(sv), axis=0)

    order = np.argsort(mean_abs)[::-1][:25]
    df_top = pd.DataFrame({
        "feature": [feature_cols[i] for i in order],
        "mean_abs_shap": mean_abs[order],
    })

    fig, ax = plt.subplots(figsize=(8, 7))
    ax.barh(df_top["feature"][::-1], df_top["mean_abs_shap"][::-1], color="#ff006e")
    ax.set_xlabel("Mean |SHAP value| (across classes)")
    ax.set_title("SHAP Feature Importance — top 25")
    plt.tight_layout()
    plt.savefig(out_path, dpi=120)
    plt.close()
    log.info(f"  saved SHAP summary -> {out_path}")
    return df_top


# ============================================================================
# report
# ============================================================================

def write_report(metrics, per_class, top_features, top_shap, classes,
                 train_users_count, test_users_count, feature_set):
    md = []
    md.append("# Persona Classifier — Evaluation Report\n")
    md.append(f"**Feature set:** `{feature_set}`\n")
    md.append(f"**Training users:** {train_users_count}  ")
    md.append(f"**Test users:** {test_users_count}  ")
    md.append(f"**Classes:** {len(classes)}\n")

    md.append("## Headline Metrics (held-out test set)\n")
    md.append(f"- accuracy:     **{metrics['accuracy']:.4f}**")
    md.append(f"- top-2 acc:    **{metrics['top2_accuracy']:.4f}**")
    md.append(f"- macro-F1:     **{metrics['macro_f1']:.4f}**")
    md.append(f"- weighted-F1:  **{metrics['weighted_f1']:.4f}**")
    md.append(f"- micro-F1:     **{metrics['micro_f1']:.4f}**\n")

    md.append("## Per-Persona Metrics\n")
    md.append(per_class.round(3).to_markdown())
    md.append("")

    md.append("## Top Features (CatBoost gain)\n")
    md.append(top_features.head(15).round(3).to_markdown(index=False))
    md.append("")

    if top_shap is not None:
        md.append("## Top Features (SHAP, mean |value|)\n")
        md.append(top_shap.head(15).round(4).to_markdown(index=False))
        md.append("")

    md.append("## Artifacts\n")
    md.append(f"- model:                `{cfg.MODEL_FILE}`")
    md.append(f"- label encoder:        `{cfg.LABEL_ENCODER_FILE}`")
    md.append(f"- feature columns:      `{cfg.FEATURE_LIST_FILE}`")
    md.append(f"- confusion matrix:     `{cfg.CONFUSION_PNG}`")
    md.append(f"- feature importance:   `{cfg.FEATURE_IMP_PNG}`")
    md.append(f"- shap summary:         `{cfg.SHAP_SUMMARY_PNG}`")
    md.append(f"- per-class metrics:    `{cfg.PER_CLASS_CSV}`")

    cfg.EVAL_REPORT_MD.write_text("\n".join(md), encoding="utf-8")
    log.info(f"  saved markdown report -> {cfg.EVAL_REPORT_MD}")


# ============================================================================
# main
# ============================================================================

def main():
    cfg.banner("EVALUATE PERSONA MODEL")

    model, classes, feature_cols, feature_set = load_model_and_metadata()
    log.info(f"  model loaded.  classes={len(classes)}  features={len(feature_cols)}")
    log.info(f"  feature_set={feature_set}")

    df_test = load_test_features(feature_set)

    # filter test set to classes seen in training
    df_test = df_test[df_test["persona"].isin(classes)].reset_index(drop=True)

    metrics, per_class, cm, y_true, y_pred, proba = compute_metrics(
        model, df_test, classes, feature_cols)

    plot_confusion(cm, classes, cfg.CONFUSION_PNG)
    top_features = plot_feature_importance(model, feature_cols, cfg.FEATURE_IMP_PNG)
    top_shap = shap_summary(model, df_test, feature_cols, classes, cfg.SHAP_SUMMARY_PNG)

    train_users_count = len(pd.read_csv(cfg.TRAIN_RAW_DIR / "users.csv"))
    write_report(metrics, per_class, top_features, top_shap, classes,
                 train_users_count, len(df_test), feature_set)

    # also dump headline metrics as JSON for downstream tooling
    (cfg.REPORTS_DIR / "headline_metrics.json").write_text(json.dumps(metrics, indent=2))

    log.info("DONE.")


if __name__ == "__main__":
    main()
